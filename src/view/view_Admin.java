    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        // TODO add your handling code here:
        select = tblUser.getSelectedRow();
        if (select == - 1) {
            return;
        }

        String username = tblUser.getValueAt(select, 1).toString();
        lblPermissions.setText("Change permisson " + username + ":");
        checkPermissons(username);

        cbbAdd.setSelected(false);
        cbbRemove.setSelected(false);
        cbbUpdate.setSelected(false);

        if (add == 1) {
            cbbAdd.setSelected(true);
        }
        if (remove == 1) {
            cbbRemove.setSelected(true);
        }
        if (update == 1)
            cbbUpdate.setSelected(true);
    }//GEN-LAST:event_tblUserMouseClicked

    private void btnUpdateNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateNowActionPerformed
        // TODO add your handling code here:
        String username = tblUser.getValueAt(select, 1).toString();

        int addUpdate = 0;
        int removeUpdate = 0;
        int updateUpdate = 0;

        if (cbbAdd.isSelected()) {
            addUpdate = 1;
        }
        if (cbbRemove.isSelected()) {
            removeUpdate = 1;
        }
        if (cbbUpdate.isSelected()) {
            updateUpdate = 1;
        }

        model_Permissons permissons = new model_Permissons(removeUpdate, updateUpdate, addUpdate);
        this.repoAdm.updatePermissons(username, permissons);
        JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
        this.loadUser();
        this.clear();
    }//GEN-LAST:event_btnUpdateNowActionPerformed

