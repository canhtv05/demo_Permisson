    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String tk = txtTK.getText().trim();
        String mk = txtMK.getText().trim();

        if (tk.isEmpty()) {
            checkEmpty();
        }

        if (mk.isEmpty()) {
            checkEmpty();
        }

        for (model_Login listAccountUser : listAccountUsers) {
            if (listAccountUser.getUsername().equalsIgnoreCase(tk)) {
                JOptionPane.showMessageDialog(this, "Da ton tai " + tk + "khong the them moi.");
                return;
            }
        }

        model_Login login = new model_Login(tk, mk);
        
        this.repoAdm.add(login);
        JOptionPane.showMessageDialog(this, "Add thanh cong");
        this.loadAccount();
        this.loadUser();
        this.clear();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String tk = txtTK.getText().trim();
        String mk = txtMK.getText().trim();

        if (tk.isEmpty()) {
            checkEmpty();
            return;
        }

        if (mk.isEmpty()) {
            checkEmpty();
            return;
        }

        boolean check = false;

        for (model_Login listAccountUser : listAccountUsers) {
            if (listAccountUser.getUsername().equalsIgnoreCase(tk)) {
                check = true;
                break;
            }
        }

        if (!check) {
            JOptionPane.showMessageDialog(this, "Khong tim thay tk " + tk + " de sua.");
            return;
        }

        model_Login login = new model_Login(tk, mk);
        this.repoAdm.update(login);
        this.loadAccount();
        JOptionPane.showMessageDialog(this, "Update thanh cong.");
        this.clear();
    }//GEN-LAST:event_btnUpdateActionPerformed

