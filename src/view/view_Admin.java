    public void loadUser() {
        listUsers = repoAdm.findAllUsers();
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);

        for (model_User user : listUsers) {
            Object[] rows = {
                user.getId(),
                user.getUsername()
            };
            model.addRow(rows);
        }
    }

    public void loadAccount() {
        listAccountUsers = repoAdm.findAllAccountUsers();
        DefaultTableModel model = (DefaultTableModel) tblUpdate.getModel();
        model.setRowCount(0);

        for (model_Login listAccountUser : listAccountUsers) {
            Object[] rows = {
                listAccountUser.getUsername(),
                listAccountUser.getPassword()
            };
            model.addRow(rows);
        }
    }

    private void checkPermissons(String username) {
        model_Permissons permissions = repoAdm.checkPermissons(username);
        if (permissions != null) {
            add = permissions.getCan_add() == 1 ? 1 : 0;
            remove = permissions.getCan_remove() == 1 ? 1 : 0;
            update = permissions.getCan_update() == 1 ? 1 : 0;
        }
    }

    private void clear() {
        cbbAdd.setSelected(false);
        cbbRemove.setSelected(false);
        cbbUpdate.setSelected(false);
        lblPermissions.setText("Change permisson:");
    }

